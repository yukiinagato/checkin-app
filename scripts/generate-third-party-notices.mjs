#!/usr/bin/env node
// Generate THIRD_PARTY_LICENSES.md by reading `pnpm licenses list --prod --json`.
// Embeds each dependency's actual license text when available.

import { execFileSync } from 'node:child_process';
import { readFileSync, existsSync, readdirSync, writeFileSync } from 'node:fs';
import { join, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const OUTPUT = join(ROOT, 'THIRD_PARTY_LICENSES.md');
const HTML_OUTPUT = join(ROOT, 'client', 'public', 'third-party-licenses.html');

const LICENSE_FILE_PATTERNS = [
  /^LICENSE(\..+)?$/i,
  /^LICENCE(\..+)?$/i,
  /^COPYING(\..+)?$/i,
  /^LICENSE-MIT$/i,
  /^LICENSE-APACHE(-2\.0)?$/i,
  /^MIT-LICENSE(\.txt)?$/i,
];

const findLicenseText = (packageDir) => {
  if (!packageDir || !existsSync(packageDir)) return null;
  let entries;
  try { entries = readdirSync(packageDir); } catch { return null; }
  const match = entries.find((name) => LICENSE_FILE_PATTERNS.some((re) => re.test(name)));
  if (!match) return null;
  try {
    return readFileSync(join(packageDir, match), 'utf8').trim();
  } catch {
    return null;
  }
};

const runPnpmLicenses = () => {
  const stdout = execFileSync('pnpm', ['licenses', 'list', '--prod', '--json'], {
    cwd: ROOT,
    encoding: 'utf8',
    maxBuffer: 64 * 1024 * 1024,
  });
  return JSON.parse(stdout);
};

const main = () => {
  const data = runPnpmLicenses();
  const packages = [];
  for (const [license, entries] of Object.entries(data)) {
    for (const entry of entries) {
      packages.push({ ...entry, license });
    }
  }
  packages.sort((a, b) => a.name.localeCompare(b.name));

  const lines = [];
  lines.push('# Third-Party Licenses');
  lines.push('');
  lines.push('This project bundles or depends on the following third-party packages.');
  lines.push('All licenses are permissive and compatible with the project license (MIT).');
  lines.push('');
  lines.push(`Generated by \`pnpm notices\` from \`pnpm licenses list --prod\`. Do not edit by hand.`);
  lines.push('');
  lines.push(`Total packages: ${packages.length}`);
  lines.push('');

  // Summary by license
  const summary = new Map();
  for (const p of packages) summary.set(p.license, (summary.get(p.license) || 0) + 1);
  lines.push('## Summary');
  lines.push('');
  lines.push('| License | Count |');
  lines.push('|---|---|');
  for (const [license, count] of [...summary.entries()].sort((a, b) => b[1] - a[1])) {
    lines.push(`| ${license} | ${count} |`);
  }
  lines.push('');

  lines.push('## Packages');
  lines.push('');
  for (const p of packages) {
    const versions = Array.isArray(p.versions) ? p.versions.join(', ') : '';
    const homepage = p.homepage ? ` — [${p.homepage}](${p.homepage})` : '';
    lines.push(`### ${p.name}@${versions}`);
    lines.push('');
    lines.push(`- License: **${p.license}**`);
    if (p.author) lines.push(`- Author: ${typeof p.author === 'string' ? p.author : (p.author?.name || JSON.stringify(p.author))}`);
    if (p.homepage) lines.push(`- Homepage: ${p.homepage}`);
    if (p.description) lines.push(`- Description: ${p.description}`);
    lines.push('');
    const dir = Array.isArray(p.paths) ? p.paths[0] : p.paths;
    const text = findLicenseText(dir);
    if (text) {
      lines.push('<details><summary>License text</summary>');
      lines.push('');
      lines.push('```');
      lines.push(text);
      lines.push('```');
      lines.push('');
      lines.push('</details>');
    } else {
      lines.push('_License text not bundled with this package; refer to the homepage above._');
    }
    lines.push('');
    lines.push('---');
    lines.push('');
  }

  writeFileSync(OUTPUT, lines.join('\n'));
  console.log(`Wrote ${OUTPUT} (${packages.length} packages, ${summary.size} licenses)`);

  writeFileSync(HTML_OUTPUT, renderHtml(packages, summary));
  console.log(`Wrote ${HTML_OUTPUT}`);
};

const escapeHtml = (str) =>
  String(str ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');

const renderHtml = (packages, summary) => {
  const summaryRows = [...summary.entries()]
    .sort((a, b) => b[1] - a[1])
    .map(([license, count]) => `<tr><td>${escapeHtml(license)}</td><td>${count}</td></tr>`)
    .join('');

  const packageBlocks = packages
    .map((p) => {
      const versions = Array.isArray(p.versions) ? p.versions.join(', ') : '';
      const dir = Array.isArray(p.paths) ? p.paths[0] : p.paths;
      const text = findLicenseText(dir);
      const author = typeof p.author === 'string' ? p.author : (p.author?.name || '');
      const meta = [
        `License: <strong>${escapeHtml(p.license)}</strong>`,
        author ? `Author: ${escapeHtml(author)}` : '',
        p.homepage ? `Homepage: <a href="${escapeHtml(p.homepage)}" target="_blank" rel="noopener">${escapeHtml(p.homepage)}</a>` : '',
        p.description ? `Description: ${escapeHtml(p.description)}` : '',
      ].filter(Boolean).map((line) => `<div>${line}</div>`).join('');
      const license = text
        ? `<details><summary>许可证全文</summary><pre>${escapeHtml(text)}</pre></details>`
        : `<p class="muted">License text not bundled with this package.</p>`;
      return `<section class="pkg"><h3>${escapeHtml(p.name)}@${escapeHtml(versions)}</h3>${meta}${license}</section>`;
    })
    .join('');

  return `<!doctype html>
<html lang="zh-CN">
<head>
<meta charset="utf-8" />
<meta name="viewport" content="width=device-width, initial-scale=1" />
<title>Third-Party Licenses</title>
<style>
  :root { color-scheme: light; }
  body { font-family: -apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, sans-serif; max-width: 960px; margin: 2rem auto; padding: 0 1.5rem; color: #1e293b; line-height: 1.6; }
  h1 { font-size: 1.6rem; margin-bottom: 0.4rem; }
  h2 { font-size: 1.2rem; margin-top: 2rem; padding-bottom: 0.3rem; border-bottom: 1px solid #e2e8f0; }
  h3 { font-size: 1rem; margin: 0 0 0.6rem; word-break: break-all; }
  table { border-collapse: collapse; margin: 1rem 0; }
  th, td { border: 1px solid #e2e8f0; padding: 0.4rem 0.8rem; font-size: 0.9rem; text-align: left; }
  th { background: #f8fafc; }
  .pkg { padding: 1rem 0; border-bottom: 1px solid #f1f5f9; }
  .pkg div { font-size: 0.85rem; color: #475569; }
  details { margin-top: 0.6rem; }
  summary { cursor: pointer; font-size: 0.85rem; color: #0f766e; user-select: none; }
  pre { background: #f8fafc; border: 1px solid #e2e8f0; border-radius: 6px; padding: 1rem; overflow-x: auto; white-space: pre-wrap; word-wrap: break-word; font-size: 0.78rem; line-height: 1.5; }
  .muted { color: #94a3b8; font-style: italic; font-size: 0.85rem; }
  .meta { background: #f8fafc; border: 1px solid #e2e8f0; border-radius: 8px; padding: 1rem; margin-top: 1rem; font-size: 0.9rem; }
  a { color: #0f766e; }
</style>
</head>
<body>
<h1>第三方开源许可证</h1>
<p>本应用代码以 MIT 协议发布。下列依赖均为宽松开源协议（MIT / ISC / BSD / Apache-2.0 / 0BSD / Python-2.0），其完整版权与许可文本如下。</p>
<div class="meta">
  共 <strong>${packages.length}</strong> 个依赖，${summary.size} 种许可证。本页面由 <code>pnpm notices</code> 自动生成。
</div>
<h2>许可证统计</h2>
<table><thead><tr><th>License</th><th>Count</th></tr></thead><tbody>${summaryRows}</tbody></table>
<h2>依赖明细</h2>
${packageBlocks}
</body>
</html>
`;
};

main();
